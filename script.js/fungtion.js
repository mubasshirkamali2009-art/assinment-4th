const statusbtn1  =document.getElementById("stetus-1")
const  interViewBtn1=
document.getElementById("interview-stetus-btn-1")
const  rejectedbtn1=
document.getElementById("rejected-stetus-btn-1")

 
// interview condition 
interViewBtn1.addEventListener("click",function(){
    statusbtn1.innerText="INTERVIEW"
    statusbtn1.classList.remove("text-red-500")
    statusbtn1.classList.remove("bg-red-100")
   
    statusbtn1.classList.add("text-green-500")
    statusbtn1.classList.add("bg-green-100")
}
)
rejectedbtn1.addEventListener("click",function(){
    statusbtn1.innerText="REJECTED"
    statusbtn1.classList.remove("text-green-500")
    statusbtn1.classList.remove("bg-green-100")
      statusbtn1.classList.add("bg-red-100")
    statusbtn1.classList.add("text-red-500")
})
